# 练习题：给定一个列表，通过以下五个特定操作对其进行修改：
#
# 更改元素： 将列表的第二个元素更改为 200，并打印更新后的列表。
# 追加元素： 在列表末尾添加 600 并打印新列表。
# 插入元素： 在列表的第三个位置（索引 2）插入 300，并打印结果。
# 删除元素（按值）： 从列表中删除 600 并打印列表。
# 删除元素（按索引）： 从列表中删除索引为 0 的元素，并打印列表。
# 练习目的： Python 列表是可变的，这意味着它们在创建后可以被修改。本练习演示了在程序执行期间动态“重塑”数据的各种方法。
#
# 输入内容：初始列表：[100, 50, 400, 500]
#
# 预期输出：
# 更新（更改）：[100, 200, 400, 500]
# 更新（追加）：[100, 200, 400, 500, 600]
# 更新（插入）：[100, 200, 300, 400, 500, 600]
# 更新（删除 600）：[100, 200, 300, 400, 500]
# 更新（删除索引 0）：[200, 300, 400, 500]

numbers = [100, 50, 400, 500]

numbers[1] = 200
print(numbers)
numbers.append(600)
print(numbers)
numbers.insert(2, 300)
print(numbers)
numbers.remove(600)
print(numbers)
numbers.pop(0)
print(numbers)

# 练习题：编写一个脚本，对给定的列表执行以下三个操作
#
# 访问列表的第三个元素
# 列表长度：打印项目总数
# 检查列表是否为空。
# 练习目的：在掌握复杂算法之前，必须先掌握数据访问：索引、大小和验证。快速检查列表是否包含数据可以避免“索引超出范围”错误，从而防止程序崩溃。
#
# 给定输入： numbers = [10, 20, 30, 40, 50]
#
# 预期输出：
# 第三个元素：30
# 列表长度：5
# 列表是否为空？否

numbers = [10, 20, 30, 40, 50]

print(f"第三个元素: {numbers[2]}")
print(f"列表长度: {len(numbers)}")
print(f"列表是否为空: {len(numbers) == 0}")
